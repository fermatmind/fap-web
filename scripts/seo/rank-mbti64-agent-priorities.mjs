#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";
import {
  csvEscape,
  resolveOutputPath,
  resolveRepoPath as resolveSafeRepoPath,
  sanitizeDateSlug,
} from "./artifactSafety.mjs";

const ROOT = process.cwd();
const GENERATED_DATE = sanitizeDateSlug(
  getArgValue("--generated-date") ?? process.env.MBTI64_AGENT_PRIORITY_RANKER_DATE ?? "2026-06-23",
  "generated date",
);
const GSC_IMPORT_PATH = resolveRepoPath(
  getArgValue("--gsc-import") ?? "docs/seo/personality/mbti64-seo-measurement-cohort-gsc-import-2026-06-23.json",
);
const PRIORITY_SELECTION_PATH = resolveRepoPath(
  getArgValue("--priority-selection") ??
    "docs/seo/personality/mbti64-agent-optimization-priority-selection-2026-06-23.json",
);
const QUERY_DECISION_PATH = resolveRepoPath(
  getArgValue("--query-decision") ??
    "docs/seo/personality/mbti64-agent-visible-expansion-13-query-evidence-decision-2026-06-23.json",
);
const STABILIZE_PATH = resolveRepoPath(
  getArgValue("--stabilize") ??
    "docs/seo/personality/mbti64-seo-measurement-cohort-gsc-import-stabilize-2026-06-23.json",
);
const OUTPUT_JSON = resolveOutputPath(
  ROOT,
  getArgValue("--output-json") ?? `docs/seo/personality/mbti64-agent-priority-ranker-${GENERATED_DATE}.json`,
  "output JSON path",
);
const OUTPUT_MD = resolveOutputPath(
  ROOT,
  getArgValue("--output-md") ?? `docs/seo/personality/mbti64-agent-priority-ranker-${GENERATED_DATE}.md`,
  "output Markdown path",
);
const OUTPUT_CSV = resolveOutputPath(
  ROOT,
  getArgValue("--output-csv") ?? `docs/seo/personality/mbti64-agent-priority-ranker-${GENERATED_DATE}.csv`,
  "output CSV path",
);

const ACTION_ORDER = {
  READY_QUERY_BACKED_LOW_RISK_DRAFT_REVIEW: 1,
  HOLD_QUERY_EVIDENCE_SUPPRESSED: 2,
  OBSERVE_OPTIMIZED_PILOT: 3,
  GSC_VISIBLE_OBSERVE_NO_REWRITE: 4,
  DISCOVERY_BACKLOG_GSC_NO_ROW: 5,
};

function getArgValue(name) {
  const prefix = `${name}=`;
  const found = process.argv.slice(2).find((arg) => arg.startsWith(prefix));
  return found ? found.slice(prefix.length) : null;
}

function resolveRepoPath(filePath) {
  return resolveSafeRepoPath(ROOT, filePath, "repo path");
}

function rel(filePath) {
  return path.relative(ROOT, filePath);
}

async function readJson(filePath) {
  return JSON.parse(await fs.readFile(filePath, "utf8"));
}

function byUrl(items) {
  return new Map(items.map((item) => [item.url ?? item.target_url, item]));
}

function queryDecisionByUrl(queryDecision) {
  return new Map(queryDecision.decisions.map((item) => [item.target_url, item]));
}

function exposureScore(record) {
  const impressions = Number(record.gsc?.impressions ?? 0);
  const ctr = Number(record.gsc?.ctr ?? 0);
  const clicks = Number(record.gsc?.clicks ?? 0);
  const position = Number(record.gsc?.average_position ?? 100);
  const impressionPressure = Math.log10(impressions + 1) * 20;
  const lowCtrPressure = impressions > 0 ? Math.max(0, 1 - ctr) * 20 : 0;
  const noClickPressure = impressions > 0 && clicks === 0 ? 15 : 0;
  const pageOneProximity = position > 0 && position <= 20 ? (21 - position) * 1.5 : 0;
  const topQueryOpportunity = position > 0 && position <= 5 ? 10 : 0;
  return Number((impressionPressure + lowCtrPressure + noClickPressure + pageOneProximity + topQueryOpportunity).toFixed(3));
}

function classify(record, priorityRecord, queryRecord) {
  if (queryRecord?.decision === "READY_QUERY_BACKED_LOW_RISK_DRAFT_REVIEW") {
    return {
      action_bucket: "READY_QUERY_BACKED_LOW_RISK_DRAFT_REVIEW",
      allowed_next_action: "agent_cms_draft_review_canary",
      blocker: null,
      evidence_quality: queryRecord.evidence_quality,
      reason: "Page-level GSC visibility and at least one captured query row support a low-risk draft review canary.",
    };
  }

  if (queryRecord?.decision === "HOLD_QUERY_EVIDENCE_SUPPRESSED") {
    return {
      action_bucket: "HOLD_QUERY_EVIDENCE_SUPPRESSED",
      allowed_next_action: "wait_for_gsc_api_or_manual_query_csv",
      blocker: "query_evidence_suppressed",
      evidence_quality: queryRecord.evidence_quality,
      reason: "Page-level GSC row exists, but query evidence is empty or suppressed; do not rewrite SERP copy from page metrics alone.",
    };
  }

  if (record.cohort_group === "pilot_8") {
    return {
      action_bucket: "OBSERVE_OPTIMIZED_PILOT",
      allowed_next_action: "observe_without_rewrite",
      blocker: "optimized_pilot_hold",
      evidence_quality: record.gsc?.status === "GSC_IMPORTED" ? "page_level_only" : "no_gsc_row",
      reason: "The optimized 8 pilot pages remain observation baselines and should not be regenerated by the expansion ranker.",
    };
  }

  if (record.gsc?.status === "GSC_IMPORTED" || priorityRecord?.selection_tier === "P1_AGENT_REVIEW_VISIBLE_EXPANSION") {
    return {
      action_bucket: "GSC_VISIBLE_OBSERVE_NO_REWRITE",
      allowed_next_action: "collect_query_evidence_before_draft",
      blocker: "query_evidence_required",
      evidence_quality: "page_level_only",
      reason: "The URL is visible in GSC, but lacks query-level evidence for a safe copy or FAQ recommendation.",
    };
  }

  return {
    action_bucket: "DISCOVERY_BACKLOG_GSC_NO_ROW",
    allowed_next_action: "continue_measurement",
    blocker: "gsc_row_not_observed",
    evidence_quality: "no_gsc_row",
    reason: "No page-level GSC row was present in the imported snapshot; treat as observation backlog, not zero demand.",
  };
}

function rankRecord(record, priorityRecord, queryRecord) {
  const classification = classify(record, priorityRecord, queryRecord);
  const score = exposureScore(record);
  const queryRows = queryRecord?.query_rows ?? [];
  return {
    rank: null,
    target_url: record.url,
    path: record.path,
    page_type: record.page_type,
    locale: record.locale,
    mbti_type: record.mbti_type,
    variant: record.variant,
    cohort_group: record.cohort_group,
    action_bucket: classification.action_bucket,
    allowed_next_action: classification.allowed_next_action,
    blocked_reason: classification.blocker,
    evidence_quality: classification.evidence_quality,
    priority_score: score,
    score_inputs: {
      impressions: record.gsc?.impressions ?? 0,
      clicks: record.gsc?.clicks ?? 0,
      ctr: record.gsc?.ctr ?? 0,
      average_position: record.gsc?.average_position ?? null,
      query_rows_captured: queryRows.length,
      indexnow_execution_state: record.search_queue?.indexnow?.execution_state ?? null,
      sitemap_exact_present: record.index_surfaces?.sitemap_exact_present ?? null,
      llms_exact_present: record.index_surfaces?.llms_exact_present ?? null,
      llms_full_exact_present: record.index_surfaces?.llms_full_exact_present ?? null,
    },
    query_evidence: queryRows.map((row) => ({
      query: row.query,
      clicks: Number(row.clicks ?? 0),
      impressions: Number(row.impressions ?? 0),
      ctr: Number(row.ctr ?? 0),
      position: Number(row.position ?? 0),
    })),
    prior_selection_tier: priorityRecord?.selection_tier ?? null,
    current_opportunity_tier: record.opportunity?.tier ?? null,
    reason: classification.reason,
    recommended_next_task:
      classification.action_bucket === "READY_QUERY_BACKED_LOW_RISK_DRAFT_REVIEW"
        ? "MBTI64-CMS-PROJECTION-DRAFT-VISIBLE-3-DRY-RUN-01"
        : classification.action_bucket === "HOLD_QUERY_EVIDENCE_SUPPRESSED"
          ? "MBTI64-GSC-QUERY-API-OR-MANUAL-CSV-EXPORT-10-01"
          : "next_scheduled_gsc_import",
  };
}

function bucketCounts(rows) {
  const counts = {};
  for (const row of rows) {
    counts[row.action_bucket] = (counts[row.action_bucket] ?? 0) + 1;
  }
  return counts;
}

function toCsv(rows) {
  const headers = [
    "rank",
    "path",
    "target_url",
    "locale",
    "page_type",
    "mbti_type",
    "variant",
    "cohort_group",
    "action_bucket",
    "allowed_next_action",
    "blocked_reason",
    "evidence_quality",
    "priority_score",
    "impressions",
    "clicks",
    "ctr",
    "average_position",
    "query_rows_captured",
    "recommended_next_task",
  ];
  const lines = [headers.join(",")];
  for (const row of rows) {
    const values = [
      row.rank,
      row.path,
      row.target_url,
      row.locale,
      row.page_type,
      row.mbti_type,
      row.variant ?? "",
      row.cohort_group,
      row.action_bucket,
      row.allowed_next_action,
      row.blocked_reason ?? "",
      row.evidence_quality,
      row.priority_score,
      row.score_inputs.impressions,
      row.score_inputs.clicks,
      row.score_inputs.ctr,
      row.score_inputs.average_position ?? "",
      row.score_inputs.query_rows_captured,
      row.recommended_next_task,
    ];
    lines.push(values.map((value) => csvEscape(value)).join(","));
  }
  return `${lines.join("\n")}\n`;
}

async function main() {
  const [gscImport, prioritySelection, queryDecision, stabilize] = await Promise.all([
    readJson(GSC_IMPORT_PATH),
    readJson(PRIORITY_SELECTION_PATH),
    readJson(QUERY_DECISION_PATH),
    readJson(STABILIZE_PATH),
  ]);

  const blockers = [];
  const warnings = [];

  if (gscImport.final_decision !== "PASS_GSC_IMPORTED_PRIORITY_READY") {
    blockers.push(`gsc_import_not_ready:${gscImport.final_decision}`);
  }
  if (prioritySelection.final_decision !== "PASS_PRIORITY_SELECTION_READY") {
    blockers.push(`priority_selection_not_ready:${prioritySelection.final_decision}`);
  }
  if (queryDecision.final_decision !== "PASS_VISIBLE_13_QUERY_EVIDENCE_DECISION_READY") {
    blockers.push(`query_decision_not_ready:${queryDecision.final_decision}`);
  }
  if (stabilize.final_decision !== "PASS_GSC_IMPORT_PRIORITY_PIPELINE_STABILIZED") {
    blockers.push(`stabilize_not_ready:${stabilize.final_decision}`);
  }
  if (gscImport.summary?.total !== 96) blockers.push(`expected_96_urls_found_${gscImport.summary?.total}`);
  if (queryDecision.summary?.ready_query_backed_low_risk_draft_review_count !== 3) {
    blockers.push(
      `expected_3_query_backed_ready_found_${queryDecision.summary?.ready_query_backed_low_risk_draft_review_count}`,
    );
  }
  if (queryDecision.summary?.hold_query_evidence_suppressed_count !== 10) {
    blockers.push(`expected_10_query_suppressed_found_${queryDecision.summary?.hold_query_evidence_suppressed_count}`);
  }
  if (gscImport.input_artifacts?.gsc_csv?.source_kind === "gsc_browser_page_table_snapshot") {
    warnings.push("GSC_SOURCE_PAGE_TABLE_SNAPSHOT_QUERY_DIMENSION_LIMITED");
  }

  const priorityByUrl = byUrl(prioritySelection.records);
  const queryByUrl = queryDecisionByUrl(queryDecision);
  const ranked = gscImport.records
    .map((record) => rankRecord(record, priorityByUrl.get(record.url), queryByUrl.get(record.url)))
    .sort((a, b) => {
      const orderDelta = ACTION_ORDER[a.action_bucket] - ACTION_ORDER[b.action_bucket];
      if (orderDelta !== 0) return orderDelta;
      return b.priority_score - a.priority_score || a.path.localeCompare(b.path);
    })
    .map((row, index) => ({ ...row, rank: index + 1 }));

  const readyRows = ranked.filter((row) => row.action_bucket === "READY_QUERY_BACKED_LOW_RISK_DRAFT_REVIEW");
  const heldRows = ranked.filter((row) => row.action_bucket === "HOLD_QUERY_EVIDENCE_SUPPRESSED");
  const noRowRows = ranked.filter((row) => row.action_bucket === "DISCOVERY_BACKLOG_GSC_NO_ROW");
  const pilotRows = ranked.filter((row) => row.action_bucket === "OBSERVE_OPTIMIZED_PILOT");

  const output = {
    artifact: "MBTI64-AGENT-PRIORITY-RANKER-01",
    generated_at: new Date().toISOString(),
    status: blockers.length === 0 ? "pass" : "fail",
    final_decision: blockers.length === 0 ? "PASS_AGENT_PRIORITY_RANKER_READY" : "NO_GO_AGENT_PRIORITY_RANKER_BLOCKED",
    input_artifacts: {
      gsc_import: rel(GSC_IMPORT_PATH),
      priority_selection: rel(PRIORITY_SELECTION_PATH),
      query_decision: rel(QUERY_DECISION_PATH),
      stabilize: rel(STABILIZE_PATH),
    },
    ranker_policy: {
      missing_gsc_rows_are_not_zero_demand: true,
      query_level_evidence_required_before_serp_copy_rewrite: true,
      optimized_pilot_pages_are_observation_baselines: true,
      ranker_outputs_are_recommendations_not_cms_truth: true,
      stable_action_order: Object.keys(ACTION_ORDER),
    },
    scoring_model: {
      version: "mbti64_agent_priority_ranker_v1",
      fields: ["impressions", "clicks", "ctr", "average_position", "query_rows_captured"],
      formula_note:
        "Priority score combines log impression pressure, low-CTR pressure, no-click pressure, and page-one proximity; action bucket outranks numeric score.",
    },
    summary: {
      total_urls: ranked.length,
      ready_query_backed_low_risk_draft_review: readyRows.length,
      hold_query_evidence_suppressed: heldRows.length,
      observe_optimized_pilot: pilotRows.length,
      discovery_backlog_gsc_no_row: noRowRows.length,
      page_level_gsc_imported_url_count: gscImport.summary?.with_gsc_rows ?? 0,
      query_rows_total: queryDecision.summary?.query_rows_total ?? 0,
    },
    action_bucket_counts: bucketCounts(ranked),
    ready_queue: readyRows,
    hold_queue: heldRows,
    ranked_records: ranked,
    safety_boundary: {
      cms_write_attempted: false,
      cms_live_promotion_attempted: false,
      frontend_runtime_change_attempted: false,
      search_queue_mutation_attempted: false,
      live_search_submit_attempted: false,
      sitemap_llms_mutation_attempted: false,
      gsc_api_call_attempted: false,
      gsc_request_indexing_attempted: false,
    },
    blockers,
    warnings,
    recommended_next_tasks: {
      ready_queue: "MBTI64-CMS-PROJECTION-DRAFT-VISIBLE-3-DRY-RUN-01",
      hold_queue: "MBTI64-GSC-QUERY-API-OR-MANUAL-CSV-EXPORT-10-01",
      operating_loop: "MBTI64-AGENT-RECOMMENDATION-RERUN-LOOP-01",
    },
  };

  const md = [
    "# MBTI64 Agent Priority Ranker",
    "",
    `Generated at: ${output.generated_at}`,
    "",
    "## Decision",
    "",
    `- Status: ${output.status}`,
    `- Final decision: ${output.final_decision}`,
    "",
    "## Summary",
    "",
    `- Total URLs ranked: ${output.summary.total_urls}`,
    `- Ready query-backed low-risk draft review: ${output.summary.ready_query_backed_low_risk_draft_review}`,
    `- Hold, query evidence suppressed: ${output.summary.hold_query_evidence_suppressed}`,
    `- Observe optimized pilot: ${output.summary.observe_optimized_pilot}`,
    `- Discovery backlog, no GSC row: ${output.summary.discovery_backlog_gsc_no_row}`,
    `- Page-level GSC imported URLs: ${output.summary.page_level_gsc_imported_url_count}`,
    `- Query rows captured: ${output.summary.query_rows_total}`,
    "",
    "## Ready Queue",
    "",
    ...readyRows.map(
      (row) =>
        `- ${row.rank}. ${row.path}: score ${row.priority_score}, impressions ${row.score_inputs.impressions}, query rows ${row.score_inputs.query_rows_captured}`,
    ),
    "",
    "## Hold Queue",
    "",
    ...heldRows.map((row) => `- ${row.rank}. ${row.path}: ${row.blocked_reason}`),
    "",
    "## Policy",
    "",
    "- Missing GSC rows are not zero demand.",
    "- Query-level evidence is required before SERP copy rewrite.",
    "- Optimized pilot pages remain observation baselines.",
    "- Ranker outputs are recommendations, not CMS truth.",
    "",
    "## Safety Boundary",
    "",
    "- No CMS write, live promotion, frontend runtime change, Search Queue mutation, live search submit, sitemap/llms mutation, GSC API call, or Request Indexing action was performed.",
    "",
    "## Blockers",
    "",
    ...(blockers.length ? blockers.map((item) => `- ${item}`) : ["- None"]),
    "",
    "## Warnings",
    "",
    ...(warnings.length ? warnings.map((item) => `- ${item}`) : ["- None"]),
    "",
  ].join("\n");

  await fs.mkdir(path.dirname(OUTPUT_JSON), { recursive: true });
  await fs.writeFile(OUTPUT_JSON, `${JSON.stringify(output, null, 2)}\n`);
  await fs.writeFile(OUTPUT_MD, md);
  await fs.writeFile(OUTPUT_CSV, toCsv(ranked));
  console.log(
    JSON.stringify(
      {
        output_json: OUTPUT_JSON,
        output_md: OUTPUT_MD,
        output_csv: OUTPUT_CSV,
        final_decision: output.final_decision,
        ready_count: readyRows.length,
        hold_count: heldRows.length,
      },
      null,
      2,
    ),
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
