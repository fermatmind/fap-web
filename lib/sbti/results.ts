export function toSbtiBand(score: number): SbtiBand {
  if (score >= 70) return "H";
  if (score >= 40) return "M";
  return "L";
}

export function mapSbtiScoreToResultDimensions(scores: SbtiScoreVector): SbtiResultScoreVector {
  const clamp = (value: number) => Math.max(0, Math.min(100, Math.round(value)));

  return resultVector({
    S1: clamp((scores.stability + scores.initiative + (100 - scores.signal_sensitivity)) / 3),
    S2: clamp((scores.reflection + scores.stability) / 2),
    S3: clamp((scores.initiative + scores.rhythm_control + scores.stability) / 3),
    E1: clamp((scores.stability + scores.boundary_awareness + (100 - scores.signal_sensitivity)) / 3),
    E2: clamp((scores.emotional_openness + scores.warmth + scores.social_drive) / 3),
    E3: clamp((scores.boundary_awareness + (100 - scores.emotional_openness) + scores.stability) / 3),
    A1: clamp((scores.warmth + scores.signal_sensitivity + scores.reflection) / 3),
    A2: clamp((scores.ambiguity_tolerance + (100 - scores.rhythm_control) + (100 - scores.boundary_awareness)) / 3),
    A3: clamp((scores.reflection + scores.stability + scores.rhythm_control) / 3),
    Ac1: clamp((scores.initiative + scores.novelty_seeking + scores.social_drive) / 3),
    Ac2: clamp((scores.expression_directness + scores.reflection + scores.boundary_awareness) / 3),
    Ac3: clamp((scores.rhythm_control + scores.initiative + scores.stability) / 3),
    So1: clamp((scores.social_drive + scores.group_energy + scores.playfulness) / 3),
    So2: clamp((scores.boundary_awareness + scores.signal_sensitivity + scores.stability) / 3),
    So3: clamp((scores.expression_directness + scores.emotional_openness + scores.aesthetic_showcase) / 3),
  });
}

export function resolveSbtiResultProfile(primaryTypeCode: string): SbtiResultProfile | null {
  const direct = RESULT_PROFILE_BY_CODE.get(primaryTypeCode as SbtiRawTypeCode);
  if (direct) return direct;

  const scoringArchetype = SCORING_ARCHETYPE_BY_CODE.get(primaryTypeCode);
  if (!scoringArchetype) return null;

  return RESULT_PROFILE_BY_CODE.get(scoringArchetype.profileCode) ?? null;
}

export function getSbtiHitCount(scores: SbtiScoreVector, profile: SbtiResultProfile): number {
  const mapped = mapSbtiScoreToResultDimensions(scores);
  return SBTI_RESULT_DIMENSION_KEYS.filter((key) => toSbtiBand(mapped[key]) === toSbtiBand(profile.centroid[key])).length;
}

export function getSbtiDimensionCards(scores: SbtiScoreVector, profile: SbtiResultProfile) {
  const mapped = mapSbtiScoreToResultDimensions(scores);

  return SBTI_RESULT_DIMENSIONS.map((dimension) => ({
    key: dimension.key,
    nameZh: dimension.nameZh,
    group: dimension.group,
    score: mapped[dimension.key],
    band: toSbtiBand(mapped[dimension.key]),
    oneLiner: profile.dimensionCopy[dimension.key] ?? dimensionCopy({})[dimension.key],
  }));
}

